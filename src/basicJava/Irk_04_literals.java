package basicJava;

public class Irk_04_literals {
    public static void main(String[] args) {
        byte age = 24;
        short age1 = 35;
        int age2 = 56;
        long salary = 2342322l;
        System.out.println("Integral literal ");
        System.out.println("age = "+age+ " "+"age1 = "+age1+" "+"age2 = "+age2+" salary = "+salary);
        char ch = 'A';
        System.out.println("Character literal\n"+"ch = "+(ch));
        float f1 = 345.23f;
        double d1 = 34.2345334;
        System.out.println("Decimal literal ");
        System.out.println("Float literal = "+f1);
        System.out.println("double literal = "+d1);
        boolean a = true;
        System.out.println("boolean literal = "+a);

    }
}
